export const getCategoryColor = (category: string) => {
  switch (category) {
    case "Grocery":
      return "#164E0D";
    case "Food & Beverage":
      return "#FF8900";
    case "Home & Decor":
      return "#673901";
    case "Personal Care":
      return "#FA0078";
    case "Electronics":
      return "#FF9494";
    default:
      return "#0090B0"; // Fallback color
  }
};

